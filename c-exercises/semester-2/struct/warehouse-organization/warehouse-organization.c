typedef struct {

    int codigo;
    float preco;
    int quantidade;

} reggalpao;

int agrupaMateriais(reggalpao vetor[], reggalpao unicos[]) {

    int i, j, k = 0, repetido;

    for (i = 0; i < 100; i++) {
        repetido = 0;
        for (j = 0; j < 100; j++) {
            if (vetor[i].codigo == unicos[j].codigo) {
                unicos[j].quantidade += vetor[i].quantidade;
                repetido = 1;
            }
        }

        if (repetido != 1) {
            unicos[k] = vetor[i];
            k++;
        }
    }

    return k;
}


